    private void ConsultarClientes(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultarClientes
        String nome = jTextFieldCampoNome.getText();
      DefaultTableModel tableModel = (DefaultTableModel) jTableConsultarCliente.getModel();
      tableModel.setRowCount(0);
      ClienteController clienteController = new ClienteController();
      try{
          ArrayList<Cliente> clientes = clienteController.listarClientes(nome);
          clientes.forEach((Cliente cliente) -> { 
          tableModel.addRow(new Object[] {     cliente.getCodCliente(),
                                               cliente.getNome(),
                                               cliente.getCpf(),
                                               cliente.getEmail(),
                                               cliente.getEndereco(),
                                               cliente.getNascimento()});
              });
         jTableConsultarCliente.setModel(tableModel);
      } catch (ExceptionDAO ex) {
            Logger.getLogger(TelaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ConsultarClientes

