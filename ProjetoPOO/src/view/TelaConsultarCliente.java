    private void jTableConsultarClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableConsultarClienteMouseClicked
        if(evt.getClickCount()==2){
           Integer codCliente  = (Integer) jTableConsultarCliente.getModel().getValueAt(jTableConsultarCliente.getSelectedRow(), 0);
           String nome = (String) jTableConsultarCliente.getModel().getValueAt(jTableConsultarCliente.getSelectedRow(), 1);
           String cpf = (String) jTableConsultarCliente.getModel().getValueAt(jTableConsultarCliente.getSelectedRow(), 2);
           String email = (String) jTableConsultarCliente.getModel().getValueAt(jTableConsultarCliente.getSelectedRow(), 3);
           String endereco = (String) jTableConsultarCliente.getModel().getValueAt(jTableConsultarCliente.getSelectedRow(), 4);
           Date nascimento = (Date) jTableConsultarCliente.getModel().getValueAt(jTableConsultarCliente.getSelectedRow(), 5);
           this.telaCliente.buscarCliente(WIDTH, nome, cpf, email, endereco, nascimento);
           
           this.telaCliente.setVisible(true);
           this.dispose();
       }
    }//GEN-LAST:event_jTableConsultarClienteMouseClicked

