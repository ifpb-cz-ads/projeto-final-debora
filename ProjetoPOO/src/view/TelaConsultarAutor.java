    private void jTableConsultarAutorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableConsultarAutorMouseClicked
        if(evt.getClickCount()==2){
           Integer codAutor = (Integer) jTableConsultarAutor.getModel().getValueAt(jTableConsultarAutor.getSelectedRow(), 0);
           String nome = (String) jTableConsultarAutor.getModel().getValueAt(jTableConsultarAutor.getSelectedRow(), 1);
           String nacionalidade = (String) jTableConsultarAutor.getModel().getValueAt(jTableConsultarAutor.getSelectedRow(), 2);
          
           
           this.telaAutor.buscarAtor(codAutor, nome, nacionalidade);
           
           this.telaAutor.setVisible(true);
           this.dispose();
       }
    }//GEN-LAST:event_jTableConsultarAutorMouseClicked

