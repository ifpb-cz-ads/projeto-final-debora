    private void ConsultarSearch(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultarSearch
        String nome = jTextFieldCampoNomeAutor.getText();
      DefaultTableModel tableModel = (DefaultTableModel) jTableConsultarAutor.getModel();
      tableModel.setRowCount(0);
      EscritorController escritorController = new EscritorController();
      try{
          ArrayList<Escritor> escritores = escritorController.listarAutores(nome);
          escritores.forEach((Escritor escritor) -> {
              tableModel.addRow(new Object[] { escritor.getCodAutores(),
                                               escritor.getNome(),
                                               escritor.getNacionalidade()});
                                               
              });
         jTableConsultarAutor.setModel(tableModel);
      } catch (ExceptionDAO ex) {
            Logger.getLogger(TelaAutor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ConsultarSearch

